
DROP TABLE DEPARTAMENTO CASCADE CONSTRAINTS;
DROP TABLE EMPLEADO CASCADE CONSTRAINTS;

CREATE TABLE DEPARTAMENTO
( 
  NUMEDEP   NUMBER(2) PRIMARY KEY, 
  NOMBREDEP VARCHAR2(14), 
  LOCALDEP  VARCHAR2(14) 
);


CREATE TABLE EMPLEADO
( 
  NUMEMP   NUMBER(4) PRIMARY KEY, 
  APELL    VARCHAR2(10), 
  OFICIO   VARCHAR2(10), 
  DIREMP   NUMBER(4), 
  FECHALT  DATE, 
  SALARIO  NUMBER(10), 
  COMISION NUMBER(10), 
  DEPNUME  NUMBER(2) NOT NULL, 
  CONSTRAINT EMP_NUM_FK FOREIGN KEY (DEPNUME)
       REFERENCES DEPARTAMENTO (NUMEDEP) ON DELETE CASCADE
);


